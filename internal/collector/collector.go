// Internal helpers
// ---------------------------------------------------------------------------

func (c *Collector) attachTracepoints() error {
	type tp struct {
		group   string
		name    string
		program *ebpf.Program
	}

	// TODO: update program field names after go:generate runs — these match
	// the SEC() names in ccf_probe.c converted to Go identifiers by bpf2go.
	probes := []tp{
		{"syscalls", "sys_enter_openat",   c.objs.TpOpenat},
		{"syscalls", "sys_enter_write",    c.objs.TpWrite},
		{"syscalls", "sys_enter_renameat2",c.objs.TpRename},
		{"syscalls", "sys_enter_unlinkat", c.objs.TpUnlink},
		{"sched",    "sched_process_exec", c.objs.TpExec},
		{"syscalls", "sys_enter_setuid",   c.objs.TpSetuid},
	}

	for _, p := range probes {
		l, err := link.Tracepoint(p.group, p.name, p.program, nil)
		if err != nil {
			return fmt.Errorf("attach %s/%s: %w", p.group, p.name, err)
		}
		c.links = append(c.links, l)
	}
	return nil
}

func (c *Collector) populatePIDFilter() error {
	val := uint8(1)
	for _, pid := range c.cfg.PIDBlocklist {
		if err := c.objs.PidFilter.Put(pid, val); err != nil {
			return fmt.Errorf("pid %d: %w", pid, err)
		}
	}
	return nil
}

func (c *Collector) closeLinks() {
	for _, l := range c.links {
		l.Close()
	}
	c.links = nil
}

func (c *Collector) deserialize(raw []byte) (event.RawEvent, error) {
	if len(raw) < int(unsafe.Sizeof(kernelEvent{})) {
		return event.RawEvent{}, fmt.Errorf("short record: %d bytes", len(raw))
	}

	var ke kernelEvent
	// Use little-endian (x86 / arm64 are both LE).
	ke.TimestampNS = binary.LittleEndian.Uint64(raw[0:8])
	ke.PID         = binary.LittleEndian.Uint32(raw[8:12])
	ke.PPID        = binary.LittleEndian.Uint32(raw[12:16])
	copy(ke.Comm[:], raw[16:32])
	ke.EventType   = binary.LittleEndian.Uint32(raw[32:36])
	ke.UID         = binary.LittleEndian.Uint32(raw[36:40])
	ke.GID         = binary.LittleEndian.Uint32(raw[40:44])
	copy(ke.Path[:], raw[44:300])
	copy(ke.DstPath[:], raw[300:556])

	evt := event.RawEvent{
		TimestampNS: ke.TimestampNS,
		PID:         ke.PID,
		PPID:        ke.PPID,
		ProcessName: nullTermString(ke.Comm[:]),
		UID:         ke.UID,
		GID:         ke.GID,
		Path:        nullTermString(ke.Path[:]),
		DstPath:     nullTermString(ke.DstPath[:]),
	}

	switch kernelEventType(ke.EventType) {
	case evtFileOpen:
		evt.Type = event.FileOpen
	case evtFileWrite:
		evt.Type = event.FileWrite
	case evtFileRename:
		evt.Type = event.FileRename
	case evtFileDelete:
		evt.Type = event.FileDelete
	case evtExec:
		evt.Type = event.Exec
	case evtSetUID:
		evt.Type = event.SetUID
	default:
		return event.RawEvent{}, fmt.Errorf("unknown event type: %d", ke.EventType)
	}

	return evt, nil
}

// nullTermString converts a null-terminated byte slice to a Go string.
func nullTermString(b []byte) string {
	for i, v := range b {
		if v == 0 {
			return string(b[:i])
		}
	}
	return string(b)
}
